"""
       Суммирует две строки и возвращает новую строку.
        """


def text(a, b):
    """
    Суммирует две строки.

    Parameters
    ----------
    a : str
        Первая строка
    b : str
        Вторая строка

    Returns
    -------
    str
        Сумма двух строк
    """
    message = a + " " + b
    return message

